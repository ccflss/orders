package orders.vo;

public class Deposit {
	private int deposit_id;
	private String deposit_intime;
	private String deposit_outtime;
	private String deposit_num;

	public int getDeposit_id() {
		return deposit_id;
	}
	public void setDeposit_id(int deposit_id) {
		this.deposit_id = deposit_id;
	}
	public String getDeposit_intime() {
		return deposit_intime;
	}
	public void setDeposit_intime(String deposit_intime) {
		this.deposit_intime = deposit_intime;
	}
	public String getDeposit_outtime() {
		return deposit_outtime;
	}
	public void setDeposit_outtime(String deposit_outtime) {
		this.deposit_outtime = deposit_outtime;
	}
	public String getDeposit_num() {
		return deposit_num;
	}
	public void setDeposit_num(String deposit_num) {
		this.deposit_num = deposit_num;
	}

	
	
	
	
}
